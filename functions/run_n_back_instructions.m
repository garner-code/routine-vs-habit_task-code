function [] = run_n_back_instructions(window, screenYpixels, time)

Screen('TextStyle', window, 1);
Screen('TextSize', window, 20);
instruct_col = [49, 130, 189];

instructions = ...
    sprintf(['Nice one!\n\n'...
             'While we give the animals some time to rest, you are now going\n' ...
             'to undertake an audio-based task. \n\n'...
             'During this task, you will hear a series of letters. \n\n' ...
             'Your goal is to identify every time you hear a letter that matches\n'...
             'the letter that you heard 2 places beforehand.\n\n'...
             'Press any key to continue with the instructions.'
             ]);

DrawFormattedText(window, instructions,'Center', screenYpixels*.3, instruct_col);
Screen('Flip', window);

KbWait;
WaitSecs(time.wait_after_instruct);


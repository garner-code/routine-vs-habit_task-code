    sprintf(['For example, if you heard the letters "K", "V", "K", "Q", "Q"\n' ...
             'a correct response would be pressing the "z" key on the second "K". \n\n'...
             'During the task, you should not press the "z" key under any \n' ...
             'other circumstances \n\n' ...
             'You will now practice this audio task.\n'...
             'Place your finger on the "z" key now.\n\n'...
             'Press any key to start the task.'
             ]);

DrawFormattedText(window, instructions,'Center', screenYpixels*.3, instruct_col);
Screen('Flip', window);
